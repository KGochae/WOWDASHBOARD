function toggleRecord(item){
  const isOpen = item.classList.contains('open');
  // 다른 항목 모두 닫기
  document.querySelectorAll('.record-item').forEach(e => e.classList.remove('open'));
  if(isOpen) return; // 이미 열려있던 항목 → 접기
  item.classList.add('open');
  // lazy load: data-src → src 로 최초 1회만 로드
  const img = item.querySelector('.record-body-img');
  if(img && img.dataset.src && !img.src.includes(img.dataset.src)){
    img.src = img.dataset.src;
  }
}
